package model;

public class Sectia {
	 int id_sect;
	 int id_fac;
	 String nume_sect;
	public int getId_sect() {
		return id_sect;
	}
	public void setId_sect(int id_sect) {
		this.id_sect = id_sect;
	}
	public int getId_fac() {
		return id_fac;
	}
	public void setId_fac(int id_fac) {
		this.id_fac = id_fac;
	}
	public String getNume_sect() {
		return nume_sect;
	}
	public void setNume_sect(String nume_sect) {
		this.nume_sect = nume_sect;
	}
	 
	 

}
