package model;

public class Grupa {
	
	int id_grupa;
	int id_sect;
	String nume_grupa;
	
	public int getId_grupa() {
		return id_grupa;
	}
	public void setId_grupa(int id_grupa) {
		this.id_grupa = id_grupa;
	}
	public int getId_sect() {
		return id_sect;
	}
	public void setId_sect(int id_sect) {
		this.id_sect = id_sect;
	}
	public String getNume_grupa() {
		return nume_grupa;
	}
	public void setNume_grupa(String nume_grupa) {
		this.nume_grupa = nume_grupa;
	}
	
	

}

