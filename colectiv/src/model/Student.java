package model;

public class Student {
	private int nr_matr;
	public String blabla;
	public String nume;
	
	public String getBlabla() {
		return blabla;
	}

	public void setBlabla(String blabla) {
		this.blabla = blabla;
	}

	public String getNume() {
		return nume;
	}

	public void setNume(String nume) {
		this.nume = nume;
	}

	public int getNr_matr() {
		return nr_matr;
	}

	public void setNr_matr(int nr_matr) {
		this.nr_matr = nr_matr;
	}

}
